export function ReactorLogo({
  size = 'md',
  variant = 'light',
  showText = true,
}: ReactorLogoProps) {
  const logoSizes = {
    sm: 24,
    md: 28,
    lg: 40,
  }

  const textSizes = {
    sm: 'text-base',
    md: 'text-lg',
    lg: 'text-2xl',
  }

  const logoMarkVariant =
    variant === 'blue' ? 'blue' : variant === 'dark' ? 'light' : 'dark'

  return (
    <div className="flex items-center gap-2">
      <ReactorLogoMark size={logoSizes[size]} variant={logoMarkVariant} />
      {showText && (
        <span className={`font-semibold ${textSizes[size]} text-foreground`}>
          Reactor
        </span>
      )}
    </div>
  )
}
